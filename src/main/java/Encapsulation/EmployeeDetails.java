package Encapsulation;

public class EmployeeDetails {

	private int id;
	private String name;

	// getter and setter methods

	public void setId(int id) {

		this.id = id;
	}
	
	public int getId() {
		
		return id;
	}

	public void setName(String name) {

		this.name = name;
	}
	
	public String getName() {
		
		return name;
	}
	
}
